# AND | OR | NOT

b = 4
c = 3
print(b==c)     # False
d = b==c
print(d)        # False

print(b!=c)     # True
print(b>c)      # True


a = 7
print(a > 6 and a < 10)     #True
print(a < 6 or  a < 10)     #True

print(not(a > 6 and a < 9))     # False
print(not(a == 2 and a > 9))     # True

